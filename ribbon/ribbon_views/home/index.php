        <h2>Getting Started</h2>
        <br>
        <ol>
            <li><b>Setting the APP_ROOT:</b> By default, the app directory name is <i>ribbonphp</i>. To change your app directory name (e.g. myapp), rename the directory  and set the <code>APP_ROOT</code> variable ( <code>ribbon/ribbon_code/Config.php</code> ) from <i>ribbonphp</i> to <i>myapp</i>.</li>
            <li><b>Create a Controller:</b> By default, ribbonPHP has one controller in <code>ribbon/ribbon_controllers/</code> directory, the <code>home</code> controller. Open the file and learn how to write your controller.</li>
            <li><b>Create a Model:</b> It is not necessary to have a corresponding model of each controller. By default, <code>Home_model</code> is provided as an example. Check it in <code>ribbon/ribbon_models/</code> directory.</li>
            <li><b>Create a View:</b> A view is mandatory if a method in a controller has its frontend. To create a view, go to <code>ribbon/ribbon_views/</code> directory and create a directory for the controller frontend. By default, you have <code>home</code> directory as the "container" for controller <code>home</code> frontend.</li>
        </ol>
